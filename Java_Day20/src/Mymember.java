
public class Mymember {
	public String name;
	public int age;
	public int pho;
	public String add;
	
	public Mymember( String name,int age,int pho,String add) {
		this.name=name;
		this.age=age;
		this.pho=pho;
		this.add=add;
		
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getPho() {
		return pho;
	}
	public void setPho(int pho) {
		this.pho = pho;
	}
	public String getAdd() {
		return add;
	}
	public void setAdd(String add) {
		this.add = add;
	}
	

}
